import 'package:flutter/widgets.dart';

class ScreenStyle {
  static EdgeInsetsGeometry get horizontalPadding16 => EdgeInsets.symmetric(horizontal: 16);
  static EdgeInsetsGeometry get verticalPadding16 => EdgeInsets.symmetric(vertical: 16);
  static EdgeInsetsGeometry get allPadding16 => EdgeInsets.all(16);
}
